package com.java.basics;

public class C03_WrapperClasses {

	public static void main(String[] args) {
		
		
		// All are Wrapper classes
		
		
		Byte b = 127;        // Capital B Byte wrapper class
		Short s = 32000;
		Integer i = 5000;
		Long ln = 10L;
		Float f = 1.55f;
		Double d = 1.1111;
		Character ch = 'H';
		Boolean bool = true;
		
		
		String str = "hello rohith 555";
		System.out.println(str);
		
		
		Object obj = 555;
		obj = "Object is Super Type in Java which accepts all values";
		
		

	}

}
